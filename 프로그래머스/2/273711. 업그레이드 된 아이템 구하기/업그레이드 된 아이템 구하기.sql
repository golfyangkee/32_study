-- 정답
SELECT II.ITEM_ID, II.ITEM_NAME, II.RARITY
FROM ITEM_INFO II LEFT JOIN ITEM_TREE IT USING(ITEM_ID)
WHERE IT.PARENT_ITEM_ID IN (SELECT ITEM_ID
                        FROM ITEM_INFO 
                        WHERE RARITY = 'RARE')
ORDER BY II.ITEM_ID DESC;